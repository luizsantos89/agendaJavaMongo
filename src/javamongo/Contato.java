package javamongo;

import javax.swing.JTextPane;

public class Contato {
    private JTextPane nome, email, rua, numero, complemento, bairro, cidade, estado, telResidencial, telComercial, telCelular;

    public JTextPane getNome() {
        return nome;
    }

    public void setNome(JTextPane nome) {
        this.nome = nome;
    }

    public JTextPane getEmail() {
        return email;
    }

    public void setEmail(JTextPane email) {
        this.email = email;
    }

    public JTextPane getRua() {
        return rua;
    }

    public void setRua(JTextPane rua) {
        this.rua = rua;
    }

    public JTextPane getNumero() {
        return numero;
    }

    public void setNumero(JTextPane numero) {
        this.numero = numero;
    }

    public JTextPane getComplemento() {
        return complemento;
    }

    public void setComplemento(JTextPane complemento) {
        this.complemento = complemento;
    }

    public JTextPane getBairro() {
        return bairro;
    }

    public void setBairro(JTextPane bairro) {
        this.bairro = bairro;
    }

    public JTextPane getCidade() {
        return cidade;
    }

    public void setCidade(JTextPane cidade) {
        this.cidade = cidade;
    }

    public JTextPane getEstado() {
        return estado;
    }

    public void setEstado(JTextPane estado) {
        this.estado = estado;
    }

    public JTextPane getTelResidencial() {
        return telResidencial;
    }

    public void setTelResidencial(JTextPane telResidencial) {
        this.telResidencial = telResidencial;
    }

    public JTextPane getTelComercial() {
        return telComercial;
    }

    public void setTelComercial(JTextPane telComercial) {
        this.telComercial = telComercial;
    }

    public JTextPane getTelCelular() {
        return telCelular;
    }

    public void setTelCelular(JTextPane telCelular) {
        this.telCelular = telCelular;
    }
    

    
}
